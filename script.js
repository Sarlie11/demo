            // Hàm để toggle (hiện/ẩn) CODE 1
            function toggleCode1() {
                var codeBlock = document.getElementById("codeBlock1");
                var additionalContent = document.getElementById("additionalContent1"); // Thêm phần điều khiển cho additionalContent1
            
                if (codeBlock.style.display === "none") {
                    codeBlock.style.display = "block";
                    additionalContent.style.display = "block"; // Hiện nội dung bổ sung 1
                } else {
                    codeBlock.style.display = "none";
                    additionalContent.style.display = "none"; // Ẩn nội dung bổ sung 1
                }
            }
    
